namespace HextechLoLBridge.Core.Models;

public sealed record PlayerSnapshot(
    string RiotId,
    string RiotIdGameName,
    string RiotIdTagLine,
    string ChampionName,
    string Team,
    string TeamDisplayName,
    bool IsDead,
    int Level,
    double CurrentHealth,
    double MaxHealth,
    double HealthPercent,
    string ResourceType,
    double CurrentResource,
    double MaxResource,
    double ResourcePercent,
    int Kills,
    int Deaths,
    int Assists,
    int CreepScore,
    int SkillPointsAvailable,
    LevelUpRecommendationSnapshot LevelUpRecommendation,
    IReadOnlyList<AbilitySnapshot> Abilities,
    IReadOnlyList<SummonerSpellSnapshot> SummonerSpells,
    IReadOnlyList<BuffSnapshot> ActiveBuffs,
    bool BuffDataAvailable,
    bool HasRedBuff,
    bool HasBlueBuff,
    bool HasBaronBuff,
    double RespawnTimerSeconds)
{
    public static PlayerSnapshot Empty { get; } = new(
        RiotId: "-",
        RiotIdGameName: "-",
        RiotIdTagLine: "-",
        ChampionName: "-",
        Team: "-",
        TeamDisplayName: "-",
        IsDead: false,
        Level: 0,
        CurrentHealth: 0,
        MaxHealth: 0,
        HealthPercent: 0,
        ResourceType: "-",
        CurrentResource: 0,
        MaxResource: 0,
        ResourcePercent: 0,
        Kills: 0,
        Deaths: 0,
        Assists: 0,
        CreepScore: 0,
        SkillPointsAvailable: 0,
        LevelUpRecommendation: LevelUpRecommendationSnapshot.Empty,
        Abilities: Array.Empty<AbilitySnapshot>(),
        SummonerSpells: Array.Empty<SummonerSpellSnapshot>(),
        ActiveBuffs: Array.Empty<BuffSnapshot>(),
        BuffDataAvailable: false,
        HasRedBuff: false,
        HasBlueBuff: false,
        HasBaronBuff: false,
        RespawnTimerSeconds: 0);
}
